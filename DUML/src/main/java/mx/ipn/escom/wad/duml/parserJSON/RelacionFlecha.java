package mx.ipn.escom.wad.duml.parserJSON;

public class RelacionFlecha {
	
	public String IdFlecha;
	public String Source;
	public String Target;
	public String getIdFlecha() {
		return IdFlecha;
	}
	public void setIdFlecha(String idFlecha) {
		IdFlecha = idFlecha;
	}
	public String getSource() {
		return Source;
	}
	public void setSource(String source) {
		Source = source;
	}
	public String getTarget() {
		return Target;
	}
	public void setTarget(String target) {
		Target = target;
	}
	
	
}
